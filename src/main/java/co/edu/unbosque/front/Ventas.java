package co.edu.unbosque.front;

public class Ventas {
	
	private Long codigo_venta;
	private Long cedula_cliente;
	private Long cedula_usuario;
	private Long ivaventa;
	private Long total_venta;
	private Long valor_vental;
	
	
	public Long getCodigo_venta() {
		return codigo_venta;
	}
	public void setCodigo_venta(Long codigo_venta) {
		this.codigo_venta = codigo_venta;
	}
	public Long getCedula_cliente() {
		return cedula_cliente;
	}
	public void setCedula_cliente(Long cedula_cliente) {
		this.cedula_cliente = cedula_cliente;
	}
	public Long getCedula_usuario() {
		return cedula_usuario;
	}
	public void setCedula_usuario(Long cedula_usuario) {
		this.cedula_usuario = cedula_usuario;
	}
	public Long getIvaventa() {
		return ivaventa;
	}
	public void setIvaventa(Long ivaventa) {
		this.ivaventa = ivaventa;
	}
	public Long getTotal_venta() {
		return total_venta;
	}
	public void setTotal_venta(Long total_venta) {
		this.total_venta = total_venta;
	}
	public Long getValor_vental() {
		return valor_vental;
	}
	public void setValor_vental(Long valor_vental) {
		this.valor_vental = valor_vental;
	}
	
	
	
	

}
